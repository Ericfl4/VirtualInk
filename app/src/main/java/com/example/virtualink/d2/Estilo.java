package com.example.virtualink.d2;


public class Estilo {
    int imageEstilo;
    String nameEstilo;


    public Estilo(int imageEstilo, String nameEstilo) {
        this.imageEstilo = imageEstilo;
        this.nameEstilo = nameEstilo;
    }

    public int getImageEstilo() {
        return imageEstilo;
    }

    public void setImageEstilo(int imageEstilo) {
        this.imageEstilo = imageEstilo;
    }

    public String getNameEstilo() {
        return nameEstilo;
    }

    public void setNameEstilo(String nameEstilo) {
        this.nameEstilo = nameEstilo;
    }
}
